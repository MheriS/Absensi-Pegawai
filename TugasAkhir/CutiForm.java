    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        Cuti c = new Cuti(CNip.getText(), CNama.getText(), tanggal.getText(), Cdurasicuti.getText(), Keterangan.getText());
        String nip = c.getNP();
        String nama = c.getNM();
        String date = c.getDaC();
        String durasi = c.getDuC();
        String keterangan = c.getK();
        
        try{
            String sql = " INSERT INTO datacuti VALUES('"+ nip + "','" + nama + "','" + date + "','" + durasi + "','" + keterangan + "')";
            java.sql.Connection conn = (Connection) AbsensiPegawai.getConnection();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil");
        } catch (HeadlessException |SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        mcuti mc = new mcuti();
        mc.close();
        DataCutiForm xx;
        xx = new DataCutiForm();
        xx.setVisible(true);
        this.dispose();
        DataCutiForm.AddRowJTable(new Object[]{
        CNip.getText(),CNama.getText(),
        tanggal.getText(),Cdurasicuti.getText(),
        Keterangan.getText(), 
        });
    }//GEN-LAST:event_SubmitActionPerformed


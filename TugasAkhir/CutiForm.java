    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        Cuti c = new Cuti();
        String mncl = c.getP();
        if(CNip.getText().equals("")){
        JOptionPane.showMessageDialog(rootPane, "Silahkan Cantumkan NIP Anda!");
        }else{
            String nip = CNip.getText();
            try{
                String sql = "SELECT `Nama` FROM `create` where NIP = '"+nip+"';";
                java.sql.Connection conn = (Connection) AbsensiPegawai.getConnection();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                ResultSet rs = pstm.executeQuery(sql);
                while (rs.next()){
                   CNama.setText(rs.getString("Nama"));
                }
                CNip.setEditable(true);
                CNama.setEditable(false);
                tanggal.setEditable(false);
                Cdurasicuti.setEditable(true);
                Keterangan.setEditable(true);
                Submit.setEnabled(true);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
    }
    }//GEN-LAST:event_searchActionPerformed

